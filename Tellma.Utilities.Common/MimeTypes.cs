namespace Tellma.Utilities.Common
{
    /// <summary>
    /// Commonly used mime type strings.
    /// </summary>
    public static class MimeTypes
    {
        /// <summary>
        /// Mime type for Microsoft Excel documents.
        /// </summary>
        public const string Excel = "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet";

        /// <summary>
        /// Mime type for Comma Separated Value (CSV) documents.
        /// </summary>
        public const string Csv = "text/csv";

        /// <summary>
        /// Mime type for HTML documents.
        /// </summary>
        public const string Html = "text/html";

        /// <summary>
        /// Mime type for plain text documents.
        /// </summary>
        public const string Text = "text/plain";
    }
}
